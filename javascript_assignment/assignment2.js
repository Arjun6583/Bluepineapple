// initialize two numbers
let number1 = 25;
let number2 = 5;

//calculate basic arithmetic operation
let addition = number1 + number2;
let subtraction = number1 - number2;
let multiplication = number1 * number2;
let division = number1 / number2;

// printing all arithmetic operation result

console.log("Addition:", addition);
console.log("Subtraction:", subtraction);
console.log("Multiplication:", multiplication);
console.log("Division:", division);

//check given number is greater than 10 or not
if (division > 10) {
    console.log("The result of the division is greater than 10.");
} else {
    console.log("The result of the division is less than or equal to 10.");
}
